# 3.2-add-subtract-methods.py

import numpy as np;

a = np.array([10,20,30,40,50,60])
b = np.array([1,2,3,4,5,6])

print(np.add(a, 5))
print(np.add(a, b))

print(np.multiply(a, 5))
print(np.multiply(a, b))

print(np.divide(a, 5))
print(np.divide(a, b))

print(np.pow(a, 5))
print(np.pow(a, b))

c = np.array([4,6,9,16,25,120])
print(np.sqrt(c))
print(np.square(a))

d = np.array([4.2,6.1,9.5,16.9,25.6,120.0])
print(np.round(d))
print(np.square(a))

